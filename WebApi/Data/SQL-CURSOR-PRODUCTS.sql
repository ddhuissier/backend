USE SHOP
GO
DECLARE @ID INT
DECLARE @NAME NVARCHAR(MAX)
DECLARE @PRICE DECIMAL(10,2)
DECLARE ProductCursor CURSOR SCROLL -- [ LOCAL | GLOBAL ] [ FORWARD_ONLY | SCROLL ] [ STATIC | KEYSET | DYNAMIC | FAST_FORWARD ] [ READ_ONLY | SCROLL_LOCKS | OPTIMISTIC ] [ TYPE_WARNING ] 
FOR SELECT Id, Name,  Price FROM PRODUCTS

OPEN ProductCursor

	FETCH FIRST FROM ProductCursor -- LAST
		INTO @ID, @NAME, @PRICE

	WHILE @@FETCH_STATUS= 0
		BEGIN

			PRINT CAST(@ID as varchar(10))+', '+ @NAME+', '+CAST(@PRICE as varchar(10))
			-- EXEC spGetProduct @ID
			FETCH NEXT FROM ProductCursor -- PRIOR
				INTO @ID, @NAME, @PRICE
		END

CLOSE ProductCursor
DEALLOCATE ProductCursor